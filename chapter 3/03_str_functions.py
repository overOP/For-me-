name = "Harry"
name1 = "pradeep chy"

print(len(name)) # print the length of the string

print(name.endswith("rry")) # check if the string ends with "rry"
print(name.startswith("Har")) # check if the string starts with "Har"

print(name.capitalize()) # capitalize the first letter of the string

print(name.lower()) # convert to lowercase
print(name.upper()) # convert to uppercase

print(name1.title()) # capitalize the first letter of each word

print(name1.strip()) # remove extra spaces from the start and end of the string

print(name1.lstrip()) # remove extra spaces from the start of the string
print(name1.rstrip()) # remove extra spaces from the end of the string

print(name1.replace("pradeep", "Roshani")) # replace "pradeep" with "Roshani"

print(name.find("a")) # find the index of the first occurrence of "a"

print(name.count("r")) # count the number of occurrences of "r"

print(name1.swapcase()) # swap the case of each letter in the string